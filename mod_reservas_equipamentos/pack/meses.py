# Pacote modulo de meses

def mes(numero):
    meses = [
        'JAN', 'FEV', 'MAR', 'ABR', 'MAI', 'JUN',
        'JUL', 'AGO', 'SET', 'OUT', 'NOV', 'DEZ'
    ]
    if numero == '01':
        return meses[0]
    elif numero == '02':
        return meses[1]
    elif numero == '03':
        return meses[2]
    elif numero == '04':
        return meses[3]
    elif numero == '05':
        return meses[4]
    elif numero == '06':
        return meses[5]
    elif numero == '07':
        return meses[6]
    elif numero == '08':
        return meses[7]
    elif numero == '09':
        return meses[8]
    elif numero == '10':
        return meses[9]
    elif numero == '11':
        return meses[10]
    elif numero == '12':
        return meses[11]
